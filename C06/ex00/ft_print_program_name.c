#include <unistd.h>

// Create a program that displays its own name followed by a new line.
// Allowed functions : write

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	(void)argc;
	ft_putstr(*argv);
	write(1, "\n", 1);
	return (0);
}
